    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        // TODO add your handling code here:
        String empID = getEmpIDField().getText().trim();
        String firstName = getFirstnameField().getText().trim();
        String lastName = getLastnameField().getText().trim();
        String empPword = getPasswordField().getText().trim();
        
        String birthDate; 
        Date bDate = getBdateField().getDate();
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try{
            birthDate = dateFormat.format(bDate);
            
        }catch(Exception e){
            OptionPane.error("Invalid. Birthday is required.");
            return;
        }
        
        String position = getPositionField().getSelectedItem().toString().trim();
        String status = "active";
        
        if(position.equalsIgnoreCase("Warehouse Manager")){
            position = "WM";
        }else if(position.equalsIgnoreCase("Purchasing Head")){
            position = "PH";
        }else if(position.equalsIgnoreCase("System Admin")){
            position = "SA";
        }
        
         if(new createUserAcctModel(empID, firstName, lastName, empPword, birthDate, position, status).addCreateUserAcctToDB(true)){}
            clearCreateUserFields();    
                System.gc();
        
    }//GEN-LAST:event_createBtnActionPerformed

